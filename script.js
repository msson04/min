function setupTodoForm() {
  const todoForm = document.getElementById('todoForm');
  if (todoForm) {
    todoForm.addEventListener('submit', function(e) {
      e.preventDefault();
      
      const todo = {
        id: Date.now(),
        text: document.getElementById('todoText').value,
        priority: document.getElementById('todoPriority').value,
        date: document.getElementById('todoDate').value,
        category: document.getElementById('todoCategory').value,
        completed: false,
        createdAt: new Date().toISOString()
      };
      
      todos.push(todo);
      
      // ÎßàÍ∞êÏùºÏù¥ ÏûàÏúºÎ©¥ Ï∫òÎ¶∞ÎçîÏóêÎèÑ Ïù¥Î≤§Ìä∏ Ï∂îÍ∞Ä
      if (todo.date) {
        const event = {
          id: Date.now() + 1,
          title: `üìã ${todo.text}`,
          date: todo.date,
          category: todo.category,
          type: 'todo',
          todoId: todo.id
        };
        events.push(event);
      }
      
      saveData();
      this.reset();
      renderTodos();
      renderDashboard();
    });
  }
}

function renderTodos() {
  const todoList = document.getElementById('todoList');
  if (!todoList) return;
  
  todoList.innerHTML = '';
  
  todos.forEach(todo => {
    const todoItem = document.createElement('div');
    todoItem.className = 'todo-item';
    
    todoItem.innerHTML = `
      <input type="checkbox" class="todo-checkbox" ${todo.completed ? 'checked' : ''} 
             onchange="toggleTodo(${todo.id})">
      <div class="todo-text ${todo.completed ? 'completed' : ''}">
        ${todo.text}
        ${todo.date ? `<br><small>üìÖ ${todo.date}</small>` : ''}
      </div>
      <span class="priority-badge priority-${todo.priority}">${getPriorityText(todo.priority)}</span>
      <button class="btn" onclick="deleteTodo(${todo.id})" style="background: var(--error); color: white; padding: 0.25rem 0.5rem;">ÏÇ≠Ï†ú</button>
    `;
    
    todoList.appendChild(todoItem);
  });
}

function toggleTodo(id) {
  const todo = todos.find(t => t.id === id);
  if (todo) {
    todo.completed = !todo.completed;
    saveData();
    renderTodos();
    renderDashboard();
  }
}

function deleteTodo(id) {
  todos = todos.filter(t => t.id !== id);
  // Ïó∞Í≤∞Îêú Ïù¥Î≤§Ìä∏ÎèÑ ÏÇ≠Ï†ú
  events = events.filter(e => e.todoId !== id);
  saveData();
  renderTodos();
  renderDashboard();
}

function getPriorityText(priority) {
  const priorityMap = {
    high: 'ÎÜíÏùå',
    medium: 'Î≥¥ÌÜµ',
    low: 'ÎÇÆÏùå'
  };
  return priorityMap[priority] || 'Î≥¥ÌÜµ';
}

